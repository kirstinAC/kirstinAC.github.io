      <h3>HONOREES</h3>
      <p>&nbsp;</p>
      <p><strong>Ron Hill TRAN '55</strong></p>
      <p>Ron Hill TRAN '55 has been an influential figure in the world of transportation design for more than four decades. Hill joined ArtCenter in 1985 as Chair of the Industrial Design Department. In 1992, he was appointed Chair of the Transportation Design Department, where he stayed until retiring in 2000. Hill&rsquo;s career began after graduating from ArtCenter in 1954. That year he joined General Motors, where he designed the rear end and fin treatment for the 1957 Eldorado Biarritz. After a stint in the U.S. Army, Hill was involved in almost every area of GM&rsquo;s design projects, and worked abroad at GM&rsquo;s affiliates in Germany, England and Brazil. As a chief designer at Chevrolet, Pontiac and Buick, he led studios that contributed to the designs of the mid-engine Corvette and the 1965 Corvair line, among many other production models. He led the 1972 and 1973 LeMans, GTO and Firebird programs, all 1973 and 1974 Buick programs and advanced Chevrolet products and advanced Cadillac projects such as the Eldorado. Hill and his team designed the forerunner to the 1978 Monte Carlo, new Camaro/Firebird designs, small car projects such as the Chevette, and the original concept vehicle for the Pontiac mid-engine Fiero project, which earned him the Industrial Design Society of America Award for Design Excellence in 1984.      </p>
