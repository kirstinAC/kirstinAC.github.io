        <h2 style="margin-top:0px;">Exhibitors</h2>
        <p style="margin-top:0px;">This year&rsquo;s Car Classic celebrates the cars, bikes, aircraft, boats, and other vehicles that have been <em><strong>Inspired by Nature</strong></em> &mdash;the sculpture, form and fluidity to move through water and air that has inspired designers in the shape, function and structure of their designs over the years.</p>
        
        <hr />
        <h3>INSPIRED BY NATURE</h3>
        <img src="images/exhibitors/59_Stingray_Racer.jpg" width="100%" alt="1959 Chevrolet Corvette Racing Stingray"/>
<h4>1959 Chevrolet Corvette Racing Stingray</h4>
        <p>General Motors Design</p>
        <p>The Corvette Stingray was a privately funded concept car that formed a basis for the second (C2) generation Corvette Sting Ray. The Stingray racer-concept car was designed by ArtCenter alumnus Pete Brock, the youngest designer to work at GM at that time, Bill Mitchell, GM Vice President of styling, and another ArtCenter alumnus, Larry Shinoda in 1957. The low, flat profile resembles its aquatic namesake.</p>
        <img src="images/exhibitors/63_Sting_Ray_split.jpg" width="100%" alt="1963 Chevrolet Corvette Stingray split-window "/>
        <h4>1963 Chevrolet Corvette Stingray split-window</h4>
        <p>Bill Galloway</p>
        <p>The Stingray racer body design strongly influenced the styling of the next generation Corvette, which saw production as the 1963 &ldquo;split window&rdquo; model. The car was based on Peter Brock&rsquo;s original coupe sketch, and was refined by Larry Shinoda, again keeping the low stingray profile.</p>
        <img src="images/exhibitors/61_Mako_Shark.jpg" width="100%" alt="1961 Chevrolet Corvette Mako Shark"/>
        <h4>1961 Chevrolet Corvette Mako Shark</h4>
        <p>General Motors Design</p>
        <p>Also inspired by the 1959 Stingray racer, the XP-755 concept car, also known as the Mako Shark, was designed by ArtCenter alumnus Larry Shinoda under the direction of General Motors Styling and Design head Bill Mitchell in 1961, as a concept for future Chevrolet Corvette production cars. In keeping with the name, the streamlining, pointed snout, and other detailing was partly inspired by the sleek, fast-moving shortfin mako shark.</p>
        <img src="images/exhibitors/13_Decopods.jpg" width="100%" alt="2013 Randy Grubb Decopod scooter"/>
        <h4>2013 Randy Grubb Decopod scooter</h4>
        <p>David Johnston</p>
        <p>Known for creating enormous oversized hot rods, Randy Grubb took a completely different direction with his Decopods&mdash;one-passenger scooters with an Art Deco aluminum body reminiscent of a mollusk or aquatic shellfish. There are two styles of Decopods, a Bi-Pod and Tri-Pod. The Deco Tri-Pod utilizes a Piaggio MP3 scooter while Deco Bi-Pod is built around a Piaggio Fly 150. </p>
        <img src="images/exhibitors/63_Beetle.jpg" width="100%" alt="1963 Volkswagen Beetle Ragtop (100% electric conversion)"/>
        <h4>1963 Volkswagen Beetle Ragtop (100% electric conversion)</h4>
        <p>David Benardo/Bonnie Rodgers</p>
        <p>Volkswagen&rsquo;s classic Beetle is the most recognized car on the face of the planet, with its distinctive styling matching that of the insect for which it is named. The vehicle on display today has been converted to run on 100% electric power.</p>
        <img src="images/exhibitors/64_Airstream_Bambi-II.jpg" width="100%" alt="1964 Vintage Airstream Bambi II 16' guesthouse/camper/mobile office conversion Matthew"/>
        <h4>1964 vintage Airstream Bambi II 16' guesthouse/camper/mobile office conversion </h4>
        <p>Matthew Hofmann/Wally Hofmann, HofArc Airstreams</p>
        <p>Modern industrial design reached its pinnacle in the middle of the 1960s and the mid 1960s Airstreams are a prime example. Appearing like a reflective droplet of water on the outside and rich with desirable features on the inside, these vintage trailers continue to be sought after world wide. Weighing in at just 1950 lbs., the 1964 Bambi is one of the most compact and lightest Airstreams ever made.</p>
        <hr />
        <h3>OTHER FEATURED DESIGNS</h3>
        <img src="images/exhibitors/38_Alfa_Romeo.jpg" width="100%" alt="1938 Alfa Romeo 6C2300B Mille Miglia Berlinetta "/>
        <h4>1938 Alfa Romeo 6C2300B Mille Miglia Berlinetta</h4>
          <p>Barry Hon</p>
        <p>Originally developed especially for the 1937 Mille Miglia race, approximately 100 of the 2nd Series 6C 2300 Bs were sold from 1937&ndash;1939 after the race. It is believed that only seven were clothed with the berlinetta coachwork by Carrozzeria Touring.</p>
        <h4>1925 Rolls-Royce Silver Ghost Picadilly convertible coupe</h4>
          <p>Aaron Weiss</p>
        <p>In 1919 Rolls-Royce purchased a factory in Springfield, Massachusetts for the production of an American Silver Ghost. Initially, the car was produced as a copy of the British design, but eventually the model received more American components over the years. Standard bodies included the Pall Mall, Oxford, Picadilly Roadster, Mayfair and Riviera Town Car. In 1925 the American model used left-hand drive and used a new 3-speed transmission with a central shifter.</p>
        <h4>1939 Bugatti Type 64 Coupe</h4>
         <p>Peter and Merle Mullin</p>
        <p>In 1939, Jean Bugatti was in the process of designing the Type 64 Coupe, which was to be his greatest achievement to date. A total of three examples were started, but only one received completed bodywork before the designer's death during a Type 57 road test. The Mullin Automotive Museum has sought to finish what Bugatti started all those years ago. Using many of the same coachbuilding techniques employed by the French automaker in the late &rsquo;30s, the museum has constructed the body based on notes from Jean Bugatti, which included revolutionary &ldquo;Papillion&rdquo; doors and long dramatic fenders. ArtCenter&rsquo;s Chair of Transportation Design, Stewart Reed, has been the chief designer on this monumental project.</p>
        <img src="images/exhibitors/61_Kelly_Corvette.jpg" width="100%" alt="1961 Kelly Corvette Coupe"/>
        <h4>1961 Kelly Corvette Coupe</h4>
          <p>John Breslow</p>
        <p>This one-off 1961 Vignale Corvette was built for the 1961 Salon de l&rsquo;Automobile in Paris, France. It was based on a 1960 Corvette chassis and built by Italian coachmaker Carrozzeria Vignale. The body was designed by Gordon Kelly of the Brooks Stevens design firm.</p>
        <img src="images/exhibitors/49_Porsche_Gmund.jpg" width="100%" alt="1949 Porsche Gm&uuml;nd Coupe"/>
        <h4>1949 Porsche Gm&uuml;nd Coupe</h4>
          <p>Jeff Zwart</p>
        <p>This 1949 356/2 aluminum bodied Porsche Gm&uuml;nd Coupe is one of about 30 known to exist. It is one of less than 50 hand built all aluminum cars produced in Porsche&rsquo;s first &ldquo;factory&rdquo;&mdash; a converted sawmill in Gm&uuml;nd, Austria (hence its name). Subsequently, manufacturing was transferred to Stuttgart, Germany, where it remains to this day. The Gm&uuml;nd coupe&rsquo;s most distinctive feature is its split windshield. </p>
        <p><img src="images/exhibitors/56_Ferrari_GTZ.jpg" width="100%" alt="1956 Ferrari 250 GTZ Berlinetta Zagato"/>        </p>
        <h4>1956 Ferrari 250 GTZ Berlinetta Zagato</h4>
          <p>David Sydorick</p>
        <p>The cooperation between Ferrari and the famous coachbuilder Zagato brought a number of fascinating automobiles into the world. Zagato designed a particularly dynamic Berlinetta body for the Ferrari 250 GT. In 1956, the first sports car with the designation Ferrari 250 GTZ was created. The aim of this design was to create a superior racing car, which was also capable of creating a sensation in international beauty competitions. A key design feature is the signature Zagato &ldquo;Double Bubble&rdquo; of the roof line. </p>
        <img src="images/exhibitors/13_Deep_Orange-3.jpg" width="100%" alt="2013 Deep Orange 3"/>
          <h4>2013 Deep Orange 3</h4>
          <p>Mazda / Clemson University International Center for Automotive Research</p>
        <p>The Mazda Deep Orange 3 Concept Car is a one-off vehicle designed and built in partnership between Clemson University automotive engineering students in Greenville, North Carolina, and Transportation Design students at ArtCenter in Pasadena. Sporting a unique hybrid powertrain that can automatically switch between front-, rear-, and all-wheel drive, a gas-powered engine handles the fronts, while the rears are powered by an electric motor and a battery pack. The Deep Orange 3 also has an unusual three-plus-three layout. This was one of several &ldquo;Deep Orange&rdquo; projects that have been undertaken between the two schools.</p>
        <img src="images/exhibitors/76_Tyrrell_P34_01.jpg" width="100%" alt="1976 Tyrrell P34 F1"/>
        <h4>1976 Tyrrell P34 F1</h4>
          <p>Bruce Canepa</p>
        <p>The Tyrrell P34 (Project 34), otherwise known as the &quot;six-wheeler,&quot; was a Formula One race car designed by Derek Gardner, Tyrrell&rsquo;s chief designer. It was one of the most radical entries ever to succeed in Formula One competition and has been called the most recognizable design in the history of world motorsport. The car used four specially manufactured 10-inch-diameter (250 mm) wheels and tires at the front, with two ordinary-sized wheels at the back. The six-wheel design reduced the lift caused by two larger front wheels, improving frontal downforce. This vehicle was featured prominently in the new film Rush.</p>
        <img src="images/exhibitors/83_Ford_Barchetta.jpg" width="100%" alt="1983 Ford/Ghia Barchetta one-off concept vehicle"/>
        <h4>1982 Ford/Ghia Barchetta one-off concept vehicle</h4>
        <p>Scott Grundfor</p>
        <p>This concept car, designed by Ghia for Ford Motor Company, was unveiled at the Frankfurt Motor Show at a time when design had been greatly restricted due to a slew of new US government regulations. The design and mechanics were very fresh and the vehicle garnered rave reviews everywhere it was shown.  The Barchetta had a major impact on many designs that followed, such as the Miata, showing the industry that there was still a strong demand for affordable, fun vehicles, re-opening that market segment.</p>
        <img src="images/exhibitors/91_McLaren.jpg" width="100%" alt="1991 McLaren F1"/>
        <h4>1994 McLaren F1</h4>
          <p>Jay Leno</p>
        <p>The McLaren F1 is a sports car designed and manufactured by McLaren Automotive. It was conceived as an exercise in creating what its designers hoped would be considered the ultimate road car. Originally a concept conceived by Gordon Murray, he engaged Peter Stevens to design the exterior and interior of the car. It set the record for the world's fastest production car in the world, reaching 243 mph. The car features numerous proprietary designs and technologies and it is lighter and has a more streamlined structure than many modern sports cars. A unique feature is the driver&rsquo;s seat located in the center and slightly forward of two passengers&rsquo; seats, providing the driver with superior visibility. </p>
