        <h3>LIFETIME ACHIEVEMENT AWARD</h3>
        <h4> Tom Peters</h4>
        <p>Director, Exterior Design, Performance Cars and Full-Size Trucks</p>
        <p>Born in New Mexico, raised in Minneapolis and graduated from high school in Louisiana, Tom Peters was part of the muscle car and hot rod car culture of the 1960s. He graduated from ArtCenter College of Design in 1980 with a degree in Transportation Design and worked for General Motors right out of college. Shortly afterward he left for a job in the consumer electronics industry, but soon returned to GM to pursue his automotive passion. At GM he has spent time in both California and Detroit in a number of creative and design leadership positions, including his current position as Director of Exterior Design of performance cars and full-size trucks. Among his many accomplishments, Tom has designed an array of production and concept vehicles, including the first Saturn prototype, the Corvette Indy show vehicle, the 1988 Pontiac Banshee Prototype, the XLR Cadillac Roadster, the C6 Corvette and the brand new 2014 Corvette Stingray.</p>
        <p>&ldquo;Bringing the Stingray back has been a personal goal for well over a decade. [Our team] knew the Stingray name would have to be earned and deliver an incredible, purposeful visual impact&mdash;just as the original did in 1963. That visual impact is evident in fighter jets and the stingray animal itself. As with air craft and living forms, every transition on the Corvette Stingray surface serves a purpose executed with beauty and proportion.&rdquo; <br>
        <em>&mdash; Tom Peters</em></p>
<h3>HONORARY GUEST</h3>
        <h4>Peter Brock</h4>
        <p><em>Founder, Brock Racing Enterprises</em><em><br>
          </em><br>
          In 1985, automotive designer Peter Brock left Shelby American to start his own company, Brock Racing Enterprises (BRE). Prior to this, Brock had already achieved considerable success in the automotive design field. While working at GM Styling in 1957 he penned the classic forms that would eventually become the original 1963 &quot;split window&quot; Corvette Stingray. Later, he designed the body for the World Championship-winning Cobra Daytona Coupe for Shelby American. With BRE, he designed cars for companies like Hino, Toyota and Triumph, and created his own race team, the BRE Datsuns, which won two C Production National Championships with the 240Z and two 2.5 Trans-Am Championships with driver John Morton. Brock soon became entranced with hang gliding, and built the largest hang gliding company in the world. Brock received the Lifetime Achievement Award at Car Classic in 2011.        </p>
        <h3>FEATURED PANELISTS</h3>
        <p><strong>Randy Grubb</strong><br>
        Original Blastolene Brother</p>
        <p>Obsessed with Hot Rods since an early age, Randy Grubb enjoyed a successful 20-year career as a lamp-work paperweight artist, specializing in glass blowing. In the late &rsquo;90s he learned the art of coach building, eventually teaming with Michael Leeds on a concept that led to his completion of The Blastolene Special. The project went on to win the Chip Foose Excellence in Design Award at the Grand National Roadster Show where Jay Leno noticed the car and purchased it for his collection, renaming it the &ldquo;Tank Car.&rdquo; Grubb&rsquo;s latest project, an Art Deco scooter dubbed the Decopod, will be on display at Car Classic.</p>
        <p><strong>Glade Johnson</strong><br>
          Owner, Glade Johnson Design, Inc.</p>
        <p>Originally a concept car designer, Glade Johnson went on to work as an interior designer for the aircraft industry, ultimately founding his eponymous, Seattle-based, marine design studio in 1987, which now creates works for shipyards around the globe. Recent projects designed by the firm include the 59.5 m Oasis super yacht, the 204 ft. Altessa III motor yacht and the 58.5 m Capri motor yacht. Whether designing for air, land or sea, Johnson combines beauty with safety and design with structure, resulting in numerous awards for the best interior and exterior design of yachts.</p>
        <p><strong>Freeman Thomas</strong><br>
        Director, Strategic Concepts Group, Ford Motor Company</p>
<p>Automobile and industrial designer Freeman Thomas has held senior and leadership-level design positions with Porsche, Volkswagen Group and DaimlerChrysler, as well as Ford Motor Company where he is currently Director of Strategic Concepts Group and responsible for developing product strategies and concept vehicles for the Ford and Lincoln brands. During his remarkable career, Thomas is credited with designing the original concepts for the celebrated new Beetle (Concept 1), the Audi TT and the Chrysler 300. Most recently, he led the creation of the game-changing Start Concept. Freeman was awarded an honorary doctorate by ArtCenter College of Design. </p>
<h3>MODERATOR</h3>
        <p><strong>Susan Carpenter</strong><br>
          Automobile and Motorcycle Critic, Orange County Register</p>
        <p>Carpenter joined the <em>Orange County Register</em> from the <em>Los Angeles Times</em> in 2012, where she spent 14 years covering cars, motorcycles, green living, children&rsquo;s books, fashion and other topics. While at the <em>Times</em>, Carpenter served as an assignment editor for lifestyle topics and a general assignment senior feature writer. She also spent the latter part of her tenure at the <em>Times</em> as car and motorcycle critic. In addition to her journalistic passion, Carpenter is a longtime motorcyclist herself.</p>
<h3>EMCEES        </h3>
        
        
        <h4>Dave Kunz </h4>
        <p>Automotive Reporter, KABC-TV Channel 7 Los Angeles</p>
        
       <p> Covering the automotive beat for ABC television&rsquo;s L.A. region, as well as for affiliates in the ABC network, Kunz brings viewers the latest news on everything from practical family sedans to exotic sports cars that only a privileged few will ever drive. Kunz is also regularly involved as an emcee at annual automotive events, including the Palos Verdes Concours d&rsquo;Elegance, Newport Concours d&rsquo;Elegance, and ArtCenter College of Design&rsquo;s annual Car Classic, where he joins us again this year.</p>
        
        <h4>Barry J. Meguiar </h4>
        <p>President and CEO, Meguiar&rsquo;s, Inc. and host, <em>Car Crazy Radio</em></p>
        
       <p> Barry Meguiar was born and raised in Pasadena and is president and CEO of Meguiar&rsquo;s, Inc. His &ldquo;Car Crazy&rdquo; gene became apparent while he was a high schooler, cruising the streets of Pasadena in his &rsquo;57 Chevy and hanging out at Blair&rsquo;s Speed Shop. Back in the early 1930s, when Pasadena became a bedroom community for Hollywood and coachbuilding became part of the city&rsquo;s early claim to fame, Meguiar&rsquo;s grandfather was methodically crafting formulations for flawless automotive paint finishes. Thus the legacies of Pasadena and the Meguiar family became forever entwined. Today, Meguiar is best known as the host of Car Crazy TV on SPEED Channel, and for his Car Crazy radio program that airs in 130 markets nationwide. This year, Meguiar again serves as the emcee of our awards program.</p>
