        <p>Since its inception, ArtCenter Car Classic has examined automotive culture and vehicle architecture through the lens of design. Today and into the future, ArtCenter continues looking to cultivate relationships with those journalists who are not only interested in transportation design, but the designer behind the design&mdash;and ultimately the students who will become the designers of the future. As the College expands its curriculum to further impact the evolution of the automotive industry and the broader field of transportation, we are more than ever looking for those journalists interested in telling a comprehensive&mdash;and sometimes complex&mdash;ArtCenter story.</p>
        <p>This year's Car Classic provides several unique story opportunities, including:</p>
<ul style="list-style:disc; margin:1em;">
<li><strong>Bridging the Gap: </strong>Major car collectors support the next generation of transportation designers through philanthropy</li>
<li><strong>Full Circle, 50 Years Later:</strong> A conversation between the designer of the original 1963 Corvette Stingray and the designer of the all-new 2014 Corvette Stingray</li>
<li><strong>Inspired by Nature:</strong> The influence of our everyday surroundings on automobiles, marine design, art and architecture</li>
<li><strong>What's Past is Prologue:</strong> Future transportation designers find inspiration in classic cars</li>
</ul>

