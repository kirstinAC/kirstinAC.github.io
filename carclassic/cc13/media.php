<h3>Press Room</h3>
<p>ArtCenter College of Design is home to a world-class transportation design program educating generations of top automotive designers and mobility specialists for more than 80 years. ArtCenter&rsquo;s annual Car Classic examines our car culture and vehicle architecture through the lens of design. Not just another high-profile car show, this popular public event celebrates the very best in automotive and overall transportation design, showcasing the College&rsquo;s strong ties to industry and honoring many noteworthy alumni.</p>
