        <p><strong>1937 Cord 812 Custom Beverly</strong><br />
        Gordon Beuhrig&rsquo;s &ldquo;coffin-nosed&rdquo; Cord was the first car to have hidden headlights. It had one of the purest and simplest body forms of its era.</p>
         <img src="images/exhibitors/57AlfaSS.jpg" width="100%" alt=" " >
        <p><strong>1957 Alfa Romeo Giulietta SS</strong><br />
        Franco Scaglione&rsquo;s first production design following his 1954&ndash;56 Alfa Romeo BAT series, the Giulietta SS debuted at the 1957 Turin Motor Show.</p>
         <img src="images/exhibitors/57FiatJolly.jpg" width="100%" alt=" " >
        <p><strong>1957 Fiat Jolly</strong><br />
        While dune buggies epitomized the California beach scene, the European coastal car was the Fiat Jolly, Ghia&rsquo;s resort adaptation of the Fiat 500.</p>
         <img src="images/exhibitors/63FordTBird.jpg" width="100%" alt=" " >
        <p><strong>1963 Ford Thunderbird</strong><br />
        Stunningly futuristic for its time, the 1963 Thunderbird was designed by ArtCenter alumnus Jim Powers while a young designer at Ford.</p>
