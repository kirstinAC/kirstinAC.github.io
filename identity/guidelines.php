<div class="accordion-section">
        <a class="accordion-section-title" href="#accordion-8">Departmental Lockups</a>
         
        <div id="accordion-8" class="accordion-section-content">
            
          
			<p>We feel the best way to raise public awareness of ArtCenter is to communicate a cohesive identity through consistent use of graphic form and language. However, we also recognize the need of the various departments, offices and facilities that comprise ArtCenter to express their own individual identities. There&rsquo;s a need to maximize both consistency and flexibility.</p>
 <p>To address this need, we&rsquo;ve developed a system for creating lockups for those departments and offices in need of a specific logotype that offers a visual presentation consistent with the ArtCenter graphic identity. We take the main elements of ArtCenter&rsquo;s graphic identity&mdash;the orange dot and the typeface Neue Haas Grotesk Display Medium&mdash;and generate a set of flexible logotype lockups that are geared to the needs of specific audiences, from those who are completely unfamiliar  with ArtCenter; to people who already know the College; to those who call ArtCenter home.
</p>
           <div style="width:100%; text-align:center; padding-top:50px; padding-bottom:70px;">
           <img src="images/guidelines/dept_lockups.png" alt="" style="margin:auto;"/></div>
           
           <p>We don&rsquo;t want to prescribe any particular design approach to a department&rsquo;s print or online materials, or any specific color palette or typography; we&rsquo;re only concerned with the logotype itself. We encourage freedom and flexibility. Any office or department&rsquo;s materials can and should be as unique or subject-specific as they have always been (though in all instances ArtCenter or ArtCenter College of Design should be referenced in text to clarify the overall relationship between the office or department and the College).</p>
 <p>Because consistent use of the graphic identity will help increase brand awareness we encourage all departments to take advantage of these lockups if possible, but we understand that they can&rsquo;t address everyone&rsquo;s needs. Some academic departments may have a compelling need for a different approach to graphic identity for their specific recruitment purposes, and because of this we consider this system optional for academic departments. The Design Office will work with any academic department that requires a non-standard graphic identity to optimize its presentation as part of ArtCenter&rsquo;s overarching identity system.</p>
 <p>We also understand that ArtCenter&rsquo;s student and faculty organizations, public venues, initiatives and special events have unique audiences and may have very different communications requirements that don&rsquo;t depend on primary identification with ArtCenter, so they should not be compelled to use this system if it doesn&rsquo;t serve their needs.</p>
 <p>Third, ArtCenter&rsquo;s for-profit partnerships have a legal need to differentiate themselves from the College, and so this system is not intended for their use either.</p>
 <p>But for the other departments, offices, facilities and initiatives that provide ArtCenter&rsquo;s core services, these department-specific lockups offer a way to clearly establish their individual identities while also underscoring their essential relationship to the College as a whole.</p>
 <p>If you&rsquo;d like the Design Office to create a set of logotype lockups for your department or office, please <a href="mailto:identity@artcenter.edu">contact us</a>.</p>


           
           
           
           
           
           
           
