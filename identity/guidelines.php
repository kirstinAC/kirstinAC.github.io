    <div class="accordion-section">
        <a class="accordion-section-title" href="#accordion-5">Use of the Identity Elements</a>
         
        <div id="accordion-5" class="accordion-section-content">
            
          
			<p>The combination of a prominent dot and the name ArtCenter or ArtCenter College of Design set in Neue Haas Grotesk Medium constitutes the graphic identity. </p>
          <p>Although dot/wordmark lockups exist for certain purposes, ideally the dot and the College&rsquo;s name will not appear as a lockup, but instead should function separately. The elements can and should be responsive to the context in which they&rsquo;re used. There&rsquo;s no preferred arrangement of the name&mdash;it can be a single line or stacked, flush left, flush right or centered as the need dictates. The examples below demonstrate a few of the many possibilities for placement of the identity elements. </p>
           <div style="width:100%; text-align:center; padding-top:50px; padding-bottom:70px;">
           <img src="images/guidelines/elements.png" alt="" style="margin:auto;"/></div>
           
           <p>We advocate a flexible approach to the College&rsquo;s name and graphic identity that adapts to the communication needs of various audiences. We would recommend using the dot and the full name, &ldquo;ArtCenter College of Design,&rdquo; if the audience we&rsquo;re trying to reach is completely unfamiliar with ArtCenter, but in other situations we can be more informal. We know that &ldquo;ArtCenter&rdquo; as a name is readily understood by audiences that already have a level of familiarity with the College&mdash;friends, partners, professional peers, donors, etc. And if the audience is very familiar with ArtCenter (for example, for communications on campus and/or to current students, faculty, staff, alumni and other &ldquo;insiders&rdquo;) the dot alone will be enough to convey the College&rsquo;s identity. </p>

<p>We recommend that the shorter form of our name, ArtCenter, be emphasized over the long form whenever possible. &ldquo;ArtCenter&rdquo; can be used for an audience that&rsquo;s unfamiliar with the College as long as the full name, &ldquo;ArtCenter College of Design,&rdquo; also appears somewhere in accompanying text. </p>

<p>The dot should be orange when it can be, because when it stands on its own the color becomes an important contributor to its meaning. If the dot is printed in a color other than orange it must be accompanied by the words &ldquo;ArtCenter&rdquo; or &ldquo;ArtCenter College of Design.&rdquo;</p>


           
           
           
           
           
           
           
