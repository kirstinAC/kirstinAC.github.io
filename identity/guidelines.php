    <div class="accordion-section">
        <a class="accordion-section-title" href="#accordion-4">A Strong Typographic Voice</a>
         
        <div id="accordion-4" class="accordion-section-content">
            
          
			<p>The main typeface of the identity is Neue Haas Grotesk, a revival of the original 1957 design for Helvetica. The medium weight is used for the College&rsquo;s name in the graphic identity, but use of the entire type family is encouraged. There are  two versions of Haas Grotesk, Display and Text, that are intended for use at particular sizes. It is important that Haas Grotesk Display never be used at sizes under 14 point, and Haas Grotesk Text should never be used at sizes larger than 14 point. Relatively tight letterspacing is part of the design of Haas Grotesk, so additional tracking should only be used when absolutely necessary, and if so as little as possible should be added. In certain situations when Haas Grotesk is not available (in email signatures, for instance) it&rsquo;s permissible to substitute Helvetica  or Arial. </p>
            
            
<div style="padding-top:25px;">
<div style="float:left;"><img src="images/guidelines/typographic_voice_display.png" alt=" "></div>
<div style="float:left;"><img src="images/guidelines/typographic_voice_text.png" alt=" "></div>

</div>
		  
<p style="clear:both; padding-top:35px;">While Haas Grotesk is the official typographic voice of ArtCenter, it&rsquo;s not the only typeface that can be used in our public materials. The typeface for a particular project should be determined by the subject or context, but Haas Grotesk should remain the default typographic choice and is recommended whenever the voice of the College is required.</p>

<p>Haas Grotesk includes distinctive alternate forms of the capital &ldquo;R&rdquo; and the lowercase &ldquo;a.&rdquo; We recommend the use of these alternate letterforms whenever possible. They can be found in the glyph palette or by applying OpenType stylistic sets 1 and 2.	</p>
<div style="width:100%; text-align:center; padding-top:50px; padding-bottom:65px;">
