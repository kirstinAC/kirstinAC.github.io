      <a name="thedot"></a><h2 style="margin-top:0px;">The Dot</h2>
      
      <p>For 85 years the dot has been in nearly continuous use as a symbol representing ArtCenter, with only two exceptions: the period immediately following World War II, when the dot&mdash;particularly when it appeared in red&mdash;may have had too close an association with the Japanese flag, and for several years in the late 1980s, when the dot was put on sabbatical and then quickly reinstated after an outcry from the ArtCenter community. </p>
      <div class="mobile-image"><img src="images/history/1_1931catalog.jpg" alt="" /></div>
      <p>The form reflects the College founders&rsquo; interest in contemporary international design and <a href="#" onClick="return false;" rel="imgtip[0]">the influence of the Bauhaus</a>, but symbolically it also resonates with our Southern California location (in orange: sun, citrus, etc.) as well as the &ldquo;here&rdquo; that the word center alludes to.</p>

<a name="colororange"></a><h2>The Color Orange</h2>
<p>Orange has had an association with ArtCenter since its inception in 1930, when the front door of the school had a border of red-orange and, in the front window, <a href="#" onClick="return false;" rel="imgtip[1]">the name of the school was accompanied by a matching red-orange dot</a>. The choice of red-orange has been attributed to founder Edward A. &ldquo;Tink&rdquo; Adams&rsquo; affinity for the color, perhaps due to his love for Asian art. </p>

<div class="mobile-image"><img src="images/history/2_1947_7thSt_orange.jpg" alt="" /></div>

<p>After ArtCenter moved from its original location there was no single color identified with the College for a period of time, but orange made an official reappearance in the mid-1960s, when former president Don Kubly reintroduced it at the beginning of his leadership in reference to the College&rsquo;s formative years. Orange has been strongly associated with ArtCenter ever since, though the exact shade of orange has varied over time from the original, nearly red hue, to a less red Pantone 1655 by 2000, to the relatively yellow Pantone 021.</p>

<a name="nomenclature"></a><h2>Nomenclature</h2>
<p>ArtCenter was founded in 1930 as The Art Center School, and the name was changed to Art Center College of Design in 1965 to clarify its identity as a professional, degree-granting institution with an academic focus on both art and design. Almost from the beginning, though, it has been known informally as &ldquo;Art Center.&rdquo; This short-form name was given a new level of legitimacy in 1986, when <a href="#" onClick="return false;" rel="imgtip[2]">Kit Hinrichs&rsquo; (BFA 63) ArtCenter logotype</a>  was introduced, though after several years the words &ldquo;College of Design&rdquo; were added to the official logotype. Beginning in the late 1990s the abbreviation &ldquo;ACCD&rdquo; also came into fairly regular use for a period of time. </p>
<div class="mobile-image"><img src="images/history/3_HinrichsLogo.png" alt="" /></div>

<a name="typography"></a><h2>Typography</h2>
<p>Many different typefaces have been used in ArtCenter&rsquo;s promotional materials through the years, but until 1965, when the College began a long institutional relationship with <a href="#" onClick="return false;" rel="imgtip[3]">Helvetica</a>, no one typeface could be said to have been a part of ArtCenter&rsquo;s graphic identity. Helvetica was virtually the only typeface used in ArtCenter&rsquo;s promotional materials from 1965 through 1986, and it was further institutionalized in the mid-1970s when it was chosen for the sign program at the College&rsquo;s new Hillside Campus, parts of which are still in use today. </p>
<div class="mobile-image"><img src="images/history/4_Helvetica.png" alt="" /></div>
<div class="mobile-image"><img src="images/history/5_Futura.png" alt="" /></div>
<p>With the introduction of Hinrichs&rsquo; ArtCenter logotype, <a href="#" onClick="return false;" rel="imgtip[4]">Futura</a> became the ArtCenter identity&rsquo;s typeface, but for much of the 1990s and until 2005 Futura&rsquo;s role in the College&rsquo;s overall public presentation was overshadowed by several design vocabularies developed for promotional materials by lead designers in the ArtCenter Design Office. The most notable among these featured the typeface <a href="#" onClick="return false;" rel="imgtip[5]">Cholla</a>, which was commissioned for ArtCenter by Denise Gonzales Crisp (BFA 82) and was used almost  exclusively in all ArtCenter materials from 1998&ndash;2001, 
  in essence becoming the de facto College identity during that time. </p>
  <div class="mobile-image"><img src="images/history/6_Cholla.png" alt="" /></div>
<p>In 2006 a graphic identity designed by Takaaki Matsumoto (BFA 80) was introduced, and use of <a href="#" onClick="return false;" rel="imgtip[6]">Univers</a> was encouraged in promotional materials.</p>
<div class="mobile-image"><img src="images/history/7_Univers.png" alt="" /></div>
<a name="logotypes"></a><h2>Logotypes</h2>
<p>Throughout most of the history of ArtCenter&rsquo;s identity there was no specific typographic wordmark. Remarkably, the 1986 Hinrichs logotype was the first. In the 1990s the words &ldquo;College of Design&rdquo; were appended to Hinrichs&rsquo; original design. </p>

<div class="mobile-image"><img src="images/history/8_TakaakiLogo.png" alt="" /></div>
<p>The lockup of dot and wordmark is an even more recent development. From the 1930s until 2006, when <a href="#" onClick="return false;" rel="imgtip[7]">Takaaki Matsumoto&rsquo;s logotype</a>  was introduced, the dot and the College&rsquo;s name functioned separately. Matsumoto&rsquo;s identity was retired in 2015. </p>
