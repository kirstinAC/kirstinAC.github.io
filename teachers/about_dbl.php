	<h1>About Design-Based Learning</h1>
	<p>DBL &quot;sneaks up on learning&quot; by engaging students'
  innate curiosity and creating a fun, interactive
  environment that develops higher-level reasoning
  skills in the context of the standard K&ndash;12 curriculum. </p>
	<p>A teacher using DBL challenges students to create
	  never-before-seen solutions to specific problems.
	  For example, a sixth grade class studying ancient
	  Egypt, China, Greece and Mesopotamia might
	  begin by working in teams to build a 3D scale
	  model of a place in that ancient world. Doing so
	  compels them to answer questions such as &quot;What
	  types of dwellings will people live in?&quot; and &quot;What
	  values will they live by?&quot; Armed with their own
	  solutions, students investigate topics about these
	  societies in required texts.</p>
