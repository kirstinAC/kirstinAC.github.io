	<h1>DBL Follow-Up Seminar</h1>
<p>Building on the success of the recent Summer Institutes for Teachers and in response 
  to your request to learn more about integrating strategies from the design disciplines 
  that engage students in meaningful, higher level learning, we would like to invite all past Summer Institute for Teachers participants to attend the DBL Follow-Up Seminar.</p>
<p><strong>DBL Follow-Up Seminar: October 25, 2008 and January 10 and March 14, 2009</strong><br />
  In this dynamic seminar and planning workshop, lead faculty from the 2008 Summer Institute for Teachers will provide coaching sessions where you extend your knowledge and tailor your lessons. Finished works may be a series of design challenges with guided lessons, setting measurable criteria or practice in identifying Design Challenges 
  across all subjects.</p>
<p><strong>When and where: </strong>
The seminar will meet from 9 am&ndash;1pm in the Design-Based Learning Lab at Art Center College of Design&rsquo;s South Campus, 950 S. Raymond Avenue in Pasadena.</p>
<p><strong>Cost:
 </strong>$185 for either series includes tuition and provides two units of credit.  Transcripts are additional. Please make checks payable to Art Center College of Design.</p>
