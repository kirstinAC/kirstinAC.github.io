	<h1>Design-Based Learning  Administration &amp; Faculty</h1>
  <h2>Administration</h2>
<h3>DOREEN NELSON</h3>
<p> <strong>Hon AIA; Professor of Design-Based Learning at Art  Center College of Design; professor of education and  adjunct professor of environmental design, California State Polytechnic  University, Pomona.</strong> Developed and directs Design-Based Learning M.A. in  Education at Cal Poly Pomona. Founded Doreen Nelson&rsquo;s Design-Based Learning  formerly City Building Education in 1969. </p>
<h3>PAULA GOODMAN</h3>
<p><strong>Director, K-12 Programs, Art Center College of Design. </strong>Created <a href="http://www.artcenter.edu/academics/public-programs/artcenter-for-kids/overview.html" target="_blank"><strong>ArtCenter for Kids</strong></a>, an innovative art and design program for children   in grades 4 through 8 and helped develop the Summer   Institute for Teachers. In addition to these programs, also directs the <a href="http://www.artcenter.edu/academics/public-programs/artcenter-for-teens/overview.html" target=newwindow><b>ArtCenter for Teens program</b></a>. Formerly Associate Chair of ArtCenter's Advertising Department. Veteran K-12 teacher, Beverly Hills Unified School District.</p>
<h2>Faculty</h2>
<p><strong>NATALIE BEZDJIAN</strong> is a kindergarten teacher at Rose and Alex Armenian School in Hollywood, CA. She has been teaching for the past 17 years to students in the grades k-2nd. She earned her Bachelors of Arts Degree in Human Development from Pacific Oaks College in Pasadena, CA in May 1996. She received training in working with young children at the Children&rsquo;s school in Pasadena, CA. She earned her Master&rsquo;s degree in Design-Based Learning (DBL) from California State Polytechnic University at Pomona and is a lecturer teaching DBL in their College of Education and Integrative Studies. </p>
<p><strong>ARACELI GARCIA</strong> has been a teacher for 16 years. She has taught all levels of English courses from remedial English Language Arts to A.P. English Literature. She served as Master Teacher and Master Lead Teacher for the Hacienda-La Puente Unified School district and was a BTSA mentor. In the summer of 2011, Araceli also served as an adjunct professor for Claremont&rsquo;s Graduate School of Education Program. She is currently a member of the district&rsquo;s Common Core Task Force. She received her Bachelor&rsquo;s Degree from the University of California in San Diego and is currently working on her Master&rsquo;s Degree in Curriculum and Instruction with an emphasis on Design-Based Learning at Cal Poly Pomona. </p>
<p><strong>JENNIFER LA CERTE</strong> is a math teacher and program coordinator at Walnut High School in Walnut Valley Unified School District. She is leading the development of the Academic Design Program, implementing Design-Based Learning (DBL) in a year 10 through 12 setting, integrating math, social science, and language arts. She earned her Master&rsquo;s degree from Cal Poly Pomona in DBL in 2007 and has experience in grades 6 through 12 in math, science, social science, technology, and credit recovery. Jennifer has been a lecturer teaching DBL through the Cal Poly College of Education and Integrative Studies and Art Center. </p>
<p><strong>DOREEN NELSON</strong>, Hon. AIA, as K-12 classroom teacher and demonstration teacher for 14 years, pioneered the field of Design-Based Learning. Since1995 she leads the nation&rsquo;s first MA degree program in education in Design-Based Learning that she started at California Polytechnic University, Pomona, CA. She is professor in the College of Education and Integrative Studies, an Adjunct Professor in the College of Environmental Design at Cal Poly and a professor at Art Center College of Design, in Pasadena, CA. She is an honorary member of the American Institute of Architects, a Wang Scholar, an author, contributor to SimCity, a board member of the inner city Los Angeles Academy of Arts and Enterprise Charter High School and an advisor to Alan Kay&rsquo;s Viewpoints Research Institute. Nelson is an author and a producer of educational films. The website www.designbasedlearning.org showcases her methods. <a href="mailto:doreennelson@earthlink.net">doreennelson@earthlink.net</a></p>
<p><strong>RICHARD ROSA</strong> received his Bachelor&rsquo;s of Science Degree in Biology and his teaching credential from Michigan State University in East Lansing, Michigan. He is currently in his sixth year of teaching multiple science courses at John Muir High School in Pasadena, CA. Richard serves as science department chair and the Lead Teacher of the Business and Entrepreneurship Academy at the school. He is currently enrolled in the Masters of Arts of Education: Curriculum and Instruction program, focusing on Designed-Based Learning at Cal Poly Pomona. </p>
<p>All faculty have received an M.A. in curriculum and instruction: Design-Based Learning at California State Polytechnic  University, Pomona.</p>
