	<h1>Other ArtCenter Public Programs</h1>
    <h3><a href="http://www.artcenter.edu/academics/public-programs/artcenter-for-kids/overview.html" target="_blank"><img src="images/kids.jpg" width="160" height="103" border="1" align="left" style="margin-right:12px" />ArtCenter for Kids</a></h3>
    <p>ArtCenter for Kids offers a range of unique design courses that teach   critical thinking, innovation and visual literacy. The program aims to nurture   creativity in young children in grades 4-8 and help them to become designers of   their own worlds. Parents and teachers are encouraged to participate in the   classes at no cost.  Classes meet on Sunday afternoons, for seven weeks during the fall, spring and summer terms.   Five-day weeklong workshops are also available every summer. 
      Scholarships are available to students with financial need.<br />
    &nbsp;</p>
    <h3><a href="https://www.artcenter.edu/teens/" target="_blank"><img src="images/sat.jpg" width="160" height="103" border="1" align="left" style="margin-right:12px" />Saturday High</a></h3>
    <p>Many of ArtCenter&rsquo;s graduates began their quest for a career in art and   design in Saturday High. This high school-level program gives teens an   opportunity to explore a variety of creative disciplines and study with a   faculty of artists and designers who import the relevance of professional   practice into the classroom. Classes meet for 10 weeks and are held in the College&rsquo;s superb   facilities. Most classes meet on Saturdays, with morning and afternoon sessions   available during the fall, summer and spring terms. During the summer term,   five-day intensive workshops and several weekday courses are available. <br />
    &nbsp;</p>
    <h3><a href="https://www.artcenter.edu/acn/" target="_blank"><img src="images/atnight.jpg" width="160" height="103" border="1" align="left" style="margin-right:12px" />ArtCenter At Night</a></h3>
    <p>Whether you are an accomplished professional or a newcomer to the visual   arts, ArtCenter at Night (ACAN) offers unmatched educational opportunities to   study with a renowned faculty of art and design practitioners.</p>
    <p>ArtCenter at Night is strongly   recommended for prospective applicants to the degree program who need further   portfolio development. Up to 50 percent of full-time Art Center students attend   ACAN prior to applying to the degree programs. Scholarships are available to   students with financial need whose eventual goal is to apply to the College   fulltime.</p>
    <p> ACAN offers a varied and   ever-changing curriculum led by a creative and dedicated faculty of design   practitioners, and also offers a select range of educational programs in New   York, San Francisco and Orange County. These, too, are taught by top   professionals and held in professional art and design environments, such as   Wieden + Kennedy, Smart Design, Mazda Design North America, Cooper-Hewitt,   National Design Museum, Oakley, IDEO, and Goodby, Silverstein &amp; Partners.   </p>
