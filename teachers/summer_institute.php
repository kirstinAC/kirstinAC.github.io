	<h1 style="margin-top:0px">What if there were a proven 
        way to get your students
        excited about learning and
        improve test scores&mdash;in any
      subject?</h1>
	<h3>&nbsp;</h3>
	<h3>There is. It&rsquo;s called 
	  Design-Based Learning.</h3>
      <p>The Summer Institute for Teachers is a fiveday,
        intensive program in this time-tested
        methodology. Its goal is to tap students&rsquo; natural
        creativity to develop higher-level thinking and
        enhance comprehension of the K&ndash;12 curriculum.
        
        Full-tuition scholarships are available for this
        one-week, four semester unit course.*</p>
      <h3><strong>July 19&ndash;21 and July 24&ndash;25, 2018, 8:30 am to 4:30 pm</strong></h3>
  <ul>
  <li>For K&ndash;12 Teachers in All Subject Areas</li>
  <li>Three Units Professional Staff Development Credit</li>
  <li>Five-Day Hands-On Workshop Taught by Working Classroom Teachers</li>
    </ul>
    <h3>Award-Winning Program</h3>
<p>Founded in 2002, the Summer Institute for
  Teachers received the 2006 Award of Merit in
  K&ndash;12 Architectural Education from the American   Architectural Foundation. Also in 2006,
  ArtCenter&rsquo;s Leslie Stoltz received a California
  School Board Association Golden Bell Award for
  excellence in education for her work in Design-Based Learning with the Chapparal Middle School
  in Diamond Bar, California.</p>
<p>Design-Based Learning was developed by Doreen
  Nelson, a professor at ArtCenter College of Design
  and California State Polytechnic University, Pomona,
  and recipient of the California State University
  2006 Wang Family Excellence Award in education.
  Nelson directs the Summer Institute for Teachers.</p>
<h3>All Subjects, All Grade Levels</h3>
<p>At the Summer Institute for Teachers, you&rsquo;ll
  learn practical techniques you can immediately
  use to inspire, involve and energize your students
  and help them develop reasoning and problemsolving
  skills.</p>
<h3>Teachers Teach Teachers</h3>
<p>The faculty of the Summer Institute for Teachers
  is made up of public school educators who
  have earned a master&rsquo;s degree in Design-Based
  Learning from California State Polytechnic
  University, Pomona and have successfully put
  this innovative methodology to work in their own
  K&ndash;12 classrooms.</p>
<h3>Career Advancement</h3>
<p>Teachers who complete the Summer Institute
  for Teachers&rsquo; five-day program earn three semester
  units of professional staff development credit.
  A number of full-tuition scholarships, grants and
  group discounts are available.</p>
<h3>Supplement Your Current
  Teaching Methods</h3>
<p>Design-Based Learning is not intended to replace
  the techniques you&rsquo;re using now. Rather, it
  gives you a powerful new methodology to help
  you present challenging concepts and reach
  various types of learners, including at-risk, gifted,
  ADD and ADHD students.</p>
<h3>Fun, Interactive Workshop</h3>
<p>Like Design-Based Learning itself, the five-day
  Summer Institute for Teachers consists of
  engaging, hands-on activities that challenge and
  stimulate creativity, improving test scores and
  overall student performance.</p>
<h3>No Special Equipment Required</h3>
<p>Design-Based Learning doesn&rsquo;t require expensive
  materials. Paper, pencils, glue and recycled
  materials are all you need. As a result, Design-Based Learning programs in public schools are
  unaffected by textbook changes.</p>
<p>*Please check with your school district regarding transferability of units/salary points. Units are transferable to the M.A. Degree Program (45 units) or the Certificate Program (20 unit) in Design-Based Learning at California State Polytechnic University, Pomona.</p>
