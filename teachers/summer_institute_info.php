<h3>Grant Opportunities for Pasadena Unified School District Teachers</h3>
<p>The Pasadena Educational Foundation (PEF) is a
  nonprofit community organization that raises funds
  to support educational programs and teachers in
  the Pasadena Unified School District. PEF offers
  competitive grants to teachers for classroom
  projects and professional staff development. For
  information about the Teacher Grant Program,
  please visit www.pef1.org or check with the educational
  foundation office in your school district.</p>
<h3>Scholarships</h3>
<p>Several tuition-only scholarships are available. To apply, please include the following with your application, which must be received by <b>July 5, 2018</b>:</p>
<ol>
  <li>A letter of support from your principal. 
    (Applications from Title 1 schools will receive
    priority.)</li>
  <li>A brief (one page or less) description of how
    Design-Based Learning might contribute to your
    classroom.</li>
  <li>To receive unit credit, a check for $135 payable to ArtCenter College of Design   or credit card information on registration form.</li>
</ol>
