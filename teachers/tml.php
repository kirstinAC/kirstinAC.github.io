	<h1>Thinking Making Learning Exhibit Walk-Throughs <br />
and Conversation Series </h1>
<p>ArtCenter College of Design invites K-12 educators  to a trio of educational exhibits 
that uncover ways to sneak up on learning across the curriculum.&nbsp; A faculty team will guide you through each  exhibit and provide you with techniques for any K-12 classroom.&nbsp; Remembering how we first learned can rekindle  the joy of learning.</p>
<ul>
<li>ALYCE  DE ROULET WILLIAMSON GALLERY (Hillside Campus) <br />
<strong>INVENTING KINDERGARTEN</strong> &ndash; Norman Brosterman&rsquo;s collection of rare educational &ldquo;Gifts&rdquo; from the 1800s  co-presented by the Institute for Figuring.
<br /> <br />
  The  exhibit describes the relationship between Froebel who invented kindergarten in  Germany  in the 1830s and the modern art and architecture movement that  followed.&nbsp;&nbsp; European children played with Froebel Blocks as did  American architect Frank Lloyd Wright.&nbsp; Perhaps it is possible that  learning through play with spatial objects promotes talent.<br />
  &nbsp;<br />
</li>
<li>PUBLIC  PROGRAMS GALLERY (South Campus)&nbsp;<br />
<strong>ENVISIONING</strong> <strong>SIX-YEAR-OLD BOYS</strong> &ndash; A Design Research Case Study from the Graduate Media Design Program.<br /><br />
This exhibit  provides a peek into the lives of six-year-old boys in the context of  manliness, social intelligence, and media messaging. Media Design students  conducted a 14-week long qualitative design research study of 22 boys and their  parents. &nbsp;The exhibit demonstrates how the act of  designing can be a method for interpreting, synthesizing, and understanding  peoples' lives. <br />
&nbsp;</li>
<li>DESIGN-BASED LEARNING LABORATORY (South Campus) <br />
<strong>PATHS TO EMPOWERMENT </strong> &ndash; An  exhibition of K-12 Design-Based Learning Methods <br />
The exhibit showcases a&nbsp;K-12 methodology and actual classroom  practice that amplifies the required curriculum and produces results on  standardized tests.&nbsp; Simplified techniques from the design professions are  used to achieve the goal of Froebel and those educators like John Dewey who  followed him: productive adults who know how to think critically and problem  solve in effective teams and are able to adapt to change.</li>
</ul>
