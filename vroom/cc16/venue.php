<p>ArtCenter College of Design is located in Pasadena, California, just 10 miles from downtown Los Angeles. The College benefits from its close proximity to more than 20 advanced automotive design studios throughout Southern California, as well as companies leading the emerging fields of new mobility and alternative energies.</p>
<p>In addition to a world-renowned undergraduate Transportation Design program, and a new Graduate Transportation Systems and Design program, ArtCenter offers a wide array of industrial design and visual and communication arts programs for creative individuals who want to make a difference by offering innovative perspectives and solutions to real-world issues. </p>
<p>Situated on 175 wooded acres, ArtCenter&rsquo;s Hillside Campus provides future artists and designers with a variety of resources and technologies. The main building is a dramatic modernist steel-and-glass structure spanning an arroyo in the San Rafael Hills just above the Rose Bowl. Designed by Craig Ellwood Associates, it has been designated a local historic landmark by the City of Pasadena. </p>
